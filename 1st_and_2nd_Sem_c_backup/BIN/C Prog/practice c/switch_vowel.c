#include<stdio.h>
int main()
{
	char ch;
	printf("\nEnter Alphabet: ");
	scanf("%c",&ch);
	switch(ch)
	{
		case 'a':
			case 'A':
				case 'e':
					case 'E':
						case 'i':
							case 'I':
								case 'o':
									case 'O':
										case 'u':
											case 'U':
												printf("\nIt's a vowel");
												break;
												default:
													printf("\nIt's not a vowel");
	}
}
