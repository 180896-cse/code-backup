
class Integers
{
   public static void main(String args[])
   {
       int a=14,b=12;
       if(a>b)
       {
           System.out.println("Greater Number"+a);
           System.out.println("Smaller Number"+b);
        }
        else
        {
            System.out.println("Greater Number"+b);
            System.out.println("Smaller Number"+a);
        }
    }
}
