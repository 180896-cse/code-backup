
class PeriArea
{
  double a,c,peri ;
  public void peri ( double x , double y )
{ 
  a = x ;
  c = y ;
  peri = 2 * ( a + c ) ;
  System.out.println ( " perimeter is " + peri  );
}
  double l,b,area ;
  public void area ( double p , double q )
{
  l = p ;
  b = q ;
  area = l * b ;
  System.out.println ( " area is " + area );
}
} 
  
      
