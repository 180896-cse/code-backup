class product
{
    public static void main(int a,int b,int i)
    {
        int c;
        System.out.println("press 1 for product");
        System.out.println("press 2 for division");
        System.out.println("press 3 for remainder");
        switch(i)
        {
            case 1:
            c=a*b;
            System.out.println(c);
            break;
            case 2:
            c=a/b;
            System.out.println(c);
            break;
            case 3:
            c=a/b;
            System.out.println(c);
            break;
            default:
            System.out.println("error");
        }
    }
}
            
            