
class age
{
public static void main (int a)
{
if (a<=12)
System.out.println ("child");
else
if (a>12 && a<20)
System.out.println ("teen");
else
if (a>=20 && a<40)
System.out.println ("young");
else
if (a>=40 && a<60)
System.out.println ("mid age");
else
if (a>60)
System.out.println ("old");
}
}

