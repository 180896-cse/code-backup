// Sample program using a for statement to display 10 natural numbers
class For {
    public static void main(String args[]) {
        int ctr;
        System.out.println("The natural numbers are :");
        for(ctr = 1; ctr <= 10; ctr = ctr + 1)
            System.out.print(ctr + "    ");
        }
}