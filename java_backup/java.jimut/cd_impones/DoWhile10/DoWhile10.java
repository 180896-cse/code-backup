	import java.io.*;
	class DoWhile10 {
		public static void main(String args[]) {
			int number = 1;
			System.out.println("The numbers are...");
			do 
			{
				System.out.print(number +  "  ");
				number++;
			} while( number <= 10 );
		}
	}