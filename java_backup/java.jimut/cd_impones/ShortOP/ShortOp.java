	// Sum of two short data type numbers
	public class ShortOp {
		public static void main(String[] args) {
			short numA = 5;
			short numB = 10;
			short numC = 0;
    			numC = (short) (numA + numB);
			System.out.println("Sum of two numbers " + numA + " and " + numB 
				+ " is " + numC);
		}
	}