	// Average of two double data type values
	public class DoubleAvg {
		public static void main(String[] arg) {
			double numA = 50.0E-1;   // 5.0
			double numB = 1.0E1;     // 10.0
			double averageC = 0.0;
			averageC = (numA + numB) / 2.0;
			System.out.println("Average is : " + averageC);
		}
	}