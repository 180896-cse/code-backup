	// Demonstrate literals in Java
	class Literal {
	    public static void main(String[] args) {
	        		// Assigning values
	        		int i = 10;
	        		long Security_Num = 1L;
	        		short Mask1 = 0x007f;
	        		String Fname = "Sidharth \nSankar"; // Uses escape sequence \n
	        		char Lname = 'S';
	        		boolean flag = true;
	        		double fees = 2456.66;
	        		System.out.println("The int literal values is: " + i);
	        		System.out.println("The long literal values is: " + Security_Num);
	        		System.out.println("The short literal values is: " + Mask1);
	        		System.out.println("The string literal values is: " + Fname);
	        		System.out.println("The boolean literal values is: " + flag);
	        		System.out.println("The double literal values is: " + fees);
	    }
	}