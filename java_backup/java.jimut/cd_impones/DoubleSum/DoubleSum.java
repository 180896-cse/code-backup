	// Sum of double data type values
	public class DoubleSum {
		public static void main(String[] arg) {
			double f1 = 1.496E8;
			double f2 = 9.0E-28;
			System.out.println("First double value : " + f1);
			System.out.println("Second double value : " + f2);
			System.out.println("Sum is : " + f2 + f1);
		}
	}