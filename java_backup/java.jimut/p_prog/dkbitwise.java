import java.io.*;
class dkbitwise
{
public static void main(String[]args)
{
int a,b;
a=153;
b=135;
System.out.println("(a&b)="+(a&b));
System.out.println("(a|b)="+(a|b));
System.out.println("(a^b)="+(a^b));
System.out.println("(a<<2)="+(a<<2));
System.out.println("(12>>2)="+(12>>2));
System.out.println("(14>>>2)="+(14>>>2));
}
}
